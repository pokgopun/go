package main

import (
	"fmt"
	"log"
	"os"

	"github.com/pokgopun/go/primorial"
)

func main() {
	n := uint(10)
	if len(os.Args) > 1 {
		i, err := fmt.Sscanf(os.Args[1], "%d", &n)
		if i > 1 {
			log.Fatal(err)
		}
	}
	fmt.Println(primorial.PrimorialNums(n))
}
